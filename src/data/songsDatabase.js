export const allPopularSongs = [...popularHebrewSongs, ...popularInternationalSongs];

// Search function for local database
export const searchLocalSongs = (query, limit = 10) => {
    if (!query || query.length < 2) return [];

    const lowerQuery = query.toLowerCase();
    const results = allPopularSongs.filter(song =>
        song.name.toLowerCase().includes(lowerQuery) ||
        song.artist.toLowerCase().includes(lowerQuery) ||
        song.album.toLowerCase().includes(lowerQuery)
    );

    return results.slice(0, limit);
};
